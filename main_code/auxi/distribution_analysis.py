def jsu_plot(returns, gamma, delta, loc, scale, ticker="Asset"):
    """
    Produce a 2×2 diagnostic figure for the JSU fit:

      [0,0] Histogram + fitted PDF
      [0,1] Empirical CDF vs theoretical CDF
      [1,0] PP-plot  (theoretical quantiles on x, empirical on y)
      [1,1] QQ-plot  (theoretical quantiles on x, sample quantiles on y)

    Parameters
    ----------
    returns          : sorted 1-D numpy array of observed returns
    gamma, delta,
    loc, scale       : float – fitted JSU parameters
    ticker           : str   – label used in plot titles
    """
    returns = np.sort(np.asarray(returns, dtype=float))
    n   = len(returns)
    edf = np.arange(1, n + 1) / n

    # Fine grid for smooth curves
    x_grid = np.linspace(returns.min(), returns.max(), 500)

    fig, axes = plt.subplots(2, 2, figsize=(13, 9))
    fig.suptitle(f"{ticker} — Johnson SU Fit", fontsize=14, fontweight="bold")

    # ── [0,0]  PDF ───────────────────────────────────────────────────────────
    ax = axes[0, 0]
    ax.hist(returns, bins=60, density=True, alpha=0.45,
            color="steelblue", label="Empirical (histogram)")
    ax.plot(x_grid, jsu_pdf(x_grid, gamma, delta, loc, scale),
            color="crimson", linewidth=2, label="JSU PDF")
    ax.set_title("Density: Histogram vs JSU PDF")
    ax.set_xlabel("Returns")
    ax.set_ylabel("Density")
    ax.legend()
    ax.grid(True, alpha=0.3)

    # ── [0,1]  Inverse CDF (Quantile Function) ───────────────────────────────
    ax = axes[0, 1]
    # Empirical quantile function: probability on x, return value on y
    ax.step(edf, returns, color="black", linewidth=1.5,
            label="Empirical Quantile", where="post")
    # Theoretical quantile function: Q(p) = loc + scale * sinh((Φ⁻¹(p) − γ) / δ)
    p_grid = np.linspace(0.001, 0.999, 500)
    q_grid = loc + scale * np.sinh((stats.norm.ppf(p_grid) - gamma) / delta)
    ax.plot(p_grid, q_grid, color="crimson", linewidth=2,
            label="JSU Quantile Function", alpha=0.8)
    ax.set_title("Empirical vs Theoretical Quantile Function (Inverse CDF)")
    ax.set_xlabel("Cumulative Probability")
    ax.set_ylabel("Returns")
    ax.legend()
    ax.grid(True, alpha=0.3)

    # ── [1,0]  PP-plot ───────────────────────────────────────────────────────
    ax = axes[1, 0]
    theoretical_cdf = jsu_cdf(returns, gamma, delta, loc, scale)
    ax.scatter(theoretical_cdf, edf, s=6, color="steelblue", alpha=0.6)
    ax.plot([0, 1], [0, 1], color="crimson", linewidth=1.5,
            linestyle="--", label="Perfect fit")
    ax.set_title("PP-Plot")
    ax.set_xlabel("Theoretical CDF")
    ax.set_ylabel("Empirical CDF")
    ax.legend()
    ax.grid(True, alpha=0.3)

    # ── [1,1]  QQ-plot ───────────────────────────────────────────────────────
    ax = axes[1, 1]
    # Theoretical quantiles at the empirical probability points
    # JSU quantile: loc + scale * sinh((Phi^{-1}(p) - gamma) / delta)
    theoretical_quantiles = (loc
                             + scale * np.sinh(
                                 (stats.norm.ppf(edf) - gamma) / delta))
    ax.scatter(theoretical_quantiles, returns, s=6, color="steelblue", alpha=0.6)
    q_min = min(theoretical_quantiles.min(), returns.min())
    q_max = max(theoretical_quantiles.max(), returns.max())
    ax.plot([q_min, q_max], [q_min, q_max], color="crimson",
            linewidth=1.5, linestyle="--", label="Perfect fit")
    ax.set_title("QQ-Plot")
    ax.set_xlabel("Theoretical Quantiles (JSU)")
    ax.set_ylabel("Sample Quantiles")
    ax.legend()
    ax.grid(True, alpha=0.3)

    plt.tight_layout()
    plt.show()


