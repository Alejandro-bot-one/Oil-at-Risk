def fit_jsu(data, x0=None):
    """
    Fit the Johnson SU distribution to `data` via Maximum Likelihood Estimation
    using Nelder-Mead optimisation. (Versión Robusta)

    Parameters
    ----------
    data : array-like  – observed data (NaNs removed automatically)
    x0   : list or None – optional starting point [gamma, delta, loc, scale].
           When None, defaults to [0, 1, mean(data), std(data)].
           Pass the previous window's result for warm starts in rolling loops.
    """
    # 1. Convertir a array y ELIMINAR NaNs de forma automática
    data = np.asarray(data, dtype=float)
    data = data[~np.isnan(data)]

    n = len(data)

    # 2. Filtro de seguridad: No se puede ajustar una campana a menos de 2 puntos
    if n < 2:
        raise ValueError(f"fit_jsu requiere al menos 2 observaciones históricas. "
                         f"Solo has pasado {n}. ¿Pasaste 'realized' en lugar del histórico?")

    mean_guess = np.mean(data)
    std_guess  = np.std(data)

    # 3. Filtro de seguridad: Si todos los datos son iguales, la volatilidad es 0
    if std_guess == 0:
        std_guess = 1e-3  # Evita división por cero

    def neg_log_likelihood(params):
        gamma_p, delta_p, loc_p, scale_p = params

        # Forzar positividad de los parámetros condicionados
        delta_p = abs(delta_p)
        scale_p = abs(scale_p)

        # Evitar que el optimizador pruebe escalas minúsculas que causan Overflow
        if scale_p < 1e-6: scale_p = 1e-6
        if delta_p < 1e-6: delta_p = 1e-6

        # Suppress overflow/invalid warnings: extreme params produce 0 PDF,
        # which is handled by the floor below. Returning a large finite penalty
        # keeps the gradient signal alive and prevents NaN propagation.
        with np.errstate(over='ignore', invalid='ignore', divide='ignore'):
            pdf_vals = jsu_pdf(data, gamma_p, delta_p, loc_p, scale_p)
            ll = -np.sum(np.log(np.maximum(pdf_vals, 1e-300)))

        return ll if np.isfinite(ll) else 1e10

    warm = x0 is not None
    if x0 is None:
        x0 = [0.0, 1.0, mean_guess, std_guess]

    maxiter = 2_000 if warm else 10_000
    maxfev  = 4_000 if warm else 20_000

    res = optimize.minimize(neg_log_likelihood, x0, method='Nelder-Mead',
                            options={'xatol': 1e-8, 'fatol': 1e-8,
                                     'maxiter': maxiter, 'maxfev': maxfev})

    if not res.success:
        print(f"Warning (fit_jsu): El optimizador pudo no haber convergido. Mensaje: {res.message}")

    gamma_hat, delta_hat, loc_hat, scale_hat = res.x
    return gamma_hat, abs(delta_hat), loc_hat, abs(scale_hat)

