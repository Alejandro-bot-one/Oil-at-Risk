def skewt_pdf(x, df, a, loc, scale):
    """Probability Density Function of the Skewed-t distribution."""
    return scipy_skewt.pdf(x, df, a, loc=loc, scale=scale)

def skewt_cdf(x, df, a, loc, scale):
    """Cumulative Distribution Function of the Skewed-t distribution."""
    return scipy_skewt.cdf(x, df, a, loc=loc, scale=scale)

def skewt_sample(df, a, loc, scale, size):
    """
    Draw i.i.d. samples from a Skewed-t distribution using Inverse Transform Sampling.
    Bypasses the broken .rvs() method in statsmodels.
    """
    # 1. Generate uniform random numbers
    u = np.random.uniform(0, 1, size)
    # 2. Pass through the Inverse CDF
    samples = scipy_skewt.ppf(u, df, a, loc=loc, scale=scale)
    return np.sort(samples)

