# 3. GROWTH-AT-RISK PIPELINE (MDE & OUT-OF-SAMPLE)
# ──────────────────────────────────────────────

def mde_distfit_skewt(fcq: list[float], tau_levels: list[float] = [0.05, 0.25, 0.50, 0.75, 0.95], lit_vol: float = 2.0) -> np.array:
    """
    Minimum Distance Estimation (MDE) specifically designed for the Skewed-t distribution.
    Fits the continuous Azzalini probability density to the discrete forecasted quantiles.
    """
    emp_quantiles, taus = np.array(fcq), np.array(tau_levels)

    def loss_function(params):
        df, a, loc, scale = params
        # Strict penalty: Degrees of freedom must be > 2 to ensure finite variance in macro models
        # Scale must be strictly positive.
        if scale <= 0 or df <= 2.0: 
            return np.inf 
            
        theo_quantiles = scipy_skewt.ppf(taus, df, a, loc=loc, scale=scale)
        return np.sum((emp_quantiles - theo_quantiles)**2)

    # Initial guess: 
    # df=10.0 (Standard starting point for financial returns)
    # a=0.0 (Normal symmetry starting point)
    initial_guess = [10.0, 0.0, 0.0, lit_vol]
    
    # Bounds: df > 2.01, 'a' and 'loc' unbounded, scale > 1e-6
    bounds = [(2.01, 200.0), (None, None), (None, None), (1e-6, None)]

    with warnings.catch_warnings():
        warnings.simplefilter("ignore")
        result = minimize(loss_function, initial_guess, method='L-BFGS-B', bounds=bounds)
    
    if not result.success:
        print(f"Warning: Skewed-t MDE Optimization failed. {result.message}")

    return result.x

