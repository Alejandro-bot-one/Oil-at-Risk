"""
Distribution Analysis — Johnson SU & Azzalini Skewed-t
======================================================
Unified distribution-analysis toolkit. Provides the full fitting and diagnostic
pipeline for the two candidate return distributions used in this project:

  * Johnson SU (Johnson, 1949)          — see PART 1
  * Azzalini & Capitanio (2003) Skewed-t — see PART 2

Each family exposes the same workflow: core pdf/cdf/sampling, Maximum Likelihood
estimation, a Minimum Distance Estimation (MDE) fit to forecasted quantiles, an
end-to-end fit-and-diagnose helper, conditional-vs-unconditional comparison
plots, and an out-of-sample parameter / PIT-calibration backtest.

Naming convention
-----------------
Johnson SU helpers carry the ``jsu`` marker (``jsu_pdf``, ``fit_jsu``,
``mde_jsu``/``mde_jsu_weighted``, ``compare_distributions_jsu`` …) and the
generic OOS helpers (``generate_oos_parameters``, ``evaluate_oos_pit``) belong to
the JSU family. Skewed-t helpers carry the ``skewt`` marker (``skewt_pdf``,
``fit_skewt``, ``mde_distfit_skewt``, ``compare_distributions_skewt``,
``generate_oos_parameters_skewt``, ``evaluate_oos_pit_skewt`` …).

The downstream risk/vulnerability metrics live in ``auxi.vulnerability_metrics``
and consume ``fit_jsu`` / ``mde_jsu_weighted`` from here.

References
----------
Johnson, N. L. (1949). Systems of frequency curves generated by methods of
translation. Biometrika, 36(1/2), 149-176.
Azzalini, A., & Capitanio, A. (2003). Distributions generated by perturbation of
symmetry with emphasis on a multivariate skew t-distribution. JRSS-B, 65(2).
"""

