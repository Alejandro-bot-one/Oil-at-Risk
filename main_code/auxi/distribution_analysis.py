def jsu_pdf(x, gamma, delta, loc, scale):
    """
    Probability Density Function of the Johnson SU distribution.

    Parameters
    ----------
    x     : array-like  – evaluation points
    gamma : float       – skewness parameter
    delta : float       – shape parameter (> 0)
    loc   : float       – location parameter
    scale : float       – scale parameter (> 0)

    Returns
    -------
    pdf values (array)
    """
    x = np.asarray(x, dtype=float)
    z = (x - loc) / scale                          # standardise
    arcsinh_z = np.arcsinh(z)                      # arcsinh(z) = log(z + sqrt(z^2+1))
    kernel = gamma + delta * arcsinh_z
    return (delta / (scale * np.sqrt(2 * np.pi) * np.sqrt(1 + z**2))
            * np.exp(-0.5 * kernel**2))


def jsu_cdf(x, gamma, delta, loc, scale):
    """
    Cumulative Distribution Function of the Johnson SU distribution.

    F(x) = Φ(γ + δ · arcsinh((x − loc) / scale))

    Parameters
    ----------
    x     : array-like
    gamma : float
    delta : float  (> 0)
    loc   : float
    scale : float  (> 0)

    Returns
    -------
    cdf values in [0, 1] (array)
    """
    x = np.asarray(x, dtype=float)
    z = (x - loc) / scale
    return stats.norm.cdf(gamma + delta * np.arcsinh(z))


