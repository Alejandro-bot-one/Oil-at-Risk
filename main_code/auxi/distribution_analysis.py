def jsu_summary(gamma, delta, loc, scale):
    """
    Print a formatted summary of the fitted JSU parameters and their
    interpretations.

    Parameters
    ----------
    gamma, delta, loc, scale : float – fitted parameters
    """
    print("=" * 50)
    print("JOHNSON SU — FITTED PARAMETERS")
    print("=" * 50)
    print(f"  gamma  (skewness)      : {gamma:+.6f}")
    print(f"  delta  (shape)         : {delta:.6f}")
    print(f"  loc    (location)      : {loc:+.6f}")
    print(f"  scale  (scale)         : {scale:.6f}")
    print("-" * 50)
    # Closed-form moments of JSU (Johnson 1949)
    omega = np.exp(1.0 / delta**2)
    jsu_mean = loc - scale * np.sqrt(omega) * np.sinh(gamma / delta)
    jsu_var  = (scale**2 / 2) * (omega - 1) * (omega * np.cosh(2 * gamma / delta) + 1)
    print(f"  Implied mean           : {jsu_mean:+.6f}")
    print(f"  Implied std dev        : {np.sqrt(jsu_var):.6f}")
    skew_dir = "right (positive)" if gamma < 0 else "left (negative)"
    print(f"  Skew direction         : {skew_dir}  (gamma={gamma:+.4f})")
    heaviness = "heavy" if delta < 1 else "moderate/light"
    print(f"  Tail thickness         : {heaviness}  (delta={delta:.4f})")
    print("=" * 50)


