def fit_skewt(data):
    """
    Fits the Skewed-t distribution via Maximum Likelihood Estimation.
    Utilizes the generic continuous MLE fitter.
    """
    data = np.asarray(data, dtype=float)
    data = data[~np.isnan(data)]
    
    if len(data) < 2:
        raise ValueError("fit_skewt requires at least 2 historical observations.")

    # Returns: df, a, loc, scale
    # We provide starting guesses to help the optimizer converge and avoid warnings
    with warnings.catch_warnings():
        warnings.simplefilter("ignore")
        df_hat, a_hat, loc_hat, scale_hat = scipy_skewt.fit(
            data, 
            10.0, 0.0, # Initial guesses for df and alpha
            loc=np.mean(data), 
            scale=np.std(data)
        )
    return df_hat, a_hat, loc_hat, scale_hat

