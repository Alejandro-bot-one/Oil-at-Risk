def generate_oos_parameters_skewt(df, x, y, controls, quantiles, h, train_end_date,
                                   lit_vol=2.0):
    """
    Rolling out-of-sample loop: for each OOS date, trains a quantile regression
    on data up to that date, forecasts the h-step-ahead quantiles, then fits a
    Skewed-t via MDE to those forecasted quantiles.

    Look-ahead handling
    -------------------
    Mirrors the Johnson-SU twin ``generate_oos_parameters`` exactly. For each
    out-of-sample date the training set is truncated at ``current_date - h`` so
    that every (x_t, y_{t+h}) pair used for fitting has a target that was already
    realised on or before ``current_date`` — no future leakage. The whole
    post-``train_end_date`` window is evaluated (not just the first h days).

    Returns
    -------
    realized_oos   : list of float  – realized returns in the OOS window
    parameters_oos : list of tuple  – (df, a, loc, scale) for each OOS step
    """
    if controls is None:
        controls = []

    df_work = df.copy()
    if not isinstance(df_work.index, pd.DatetimeIndex):
        df_work.index = pd.to_datetime(df_work.index)
    df_work = df_work.sort_index()

    cols_to_keep = [y, x] + controls
    df_work = df_work[cols_to_keep]
    target_col = f"{y}_target_h{h}"
    df_work[target_col] = df_work[y].shift(-h)

    # OOS dates: every date after train_end_date whose target y_{t+h} is realised.
    df_eval = df_work.dropna(subset=[target_col]).copy()
    test_dates = df_eval.loc[df_eval.index > train_end_date].index
    if len(test_dates) == 0:
        raise ValueError("The train_end_date leaves no valid Out-of-Sample dates to evaluate.")

    realized_oos   = []
    parameters_oos = []

    for current_date in tqdm(test_dates, desc=f"OOS Skewed-t MDE (h={h})"):
        # --- LOOK-AHEAD PREVENTION: train only on pairs realised by current_date ---
        train_cutoff_idx = df_work.index.get_loc(current_date) - h
        if train_cutoff_idx < 0:
            continue
        cutoff_date = df_work.index[train_cutoff_idx]
        df_train = df_eval.loc[:cutoff_date].dropna().copy()

        features_t = df_work.loc[[current_date]]
        real_y = features_t[target_col].values[0]

        try:
            forecast_q = []
            for q in quantiles:
                with warnings.catch_warnings():
                    warnings.simplefilter("ignore")
                    try:
                        reg = q_reg(df=df_train, x=x, y=target_col, tau=q, controls=controls, vcov="robust", max_iter=2000)
                    except ValueError:
                        reg = q_reg(df=df_train, x=x, y=target_col, tau=q, controls=controls, vcov="iid", max_iter=2000)
                forecast_q.append(reg.predict(exog=features_t).values[0])

            params = mde_distfit_skewt(fcq=forecast_q, tau_levels=quantiles, lit_vol=lit_vol)
            realized_oos.append(real_y)
            parameters_oos.append(tuple(params))
        except Exception as e:
            print(f"  Skipped {current_date}: {e}")

    return realized_oos, parameters_oos


