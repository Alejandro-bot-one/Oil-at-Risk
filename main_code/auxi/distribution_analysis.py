def jsu_sample(gamma, delta, loc, scale, size):
    """
    Draw i.i.d. samples from a Johnson SU distribution.

    Uses the closed-form inverse-CDF:
        X = loc + scale * sinh((Z − gamma) / delta),  Z ~ N(0,1)

    Parameters
    ----------
    gamma, delta, loc, scale : float – distribution parameters
    size : int – number of observations to generate

    Returns
    -------
    sorted numpy array of length `size`
    """
    z = np.random.standard_normal(size)
    samples = loc + scale * np.sinh((z - gamma) / delta)
    return np.sort(samples)


