def generate_oos_parameters(df: pd.DataFrame,
                            x: str,
                            y: str,
                            controls: list[str],
                            quantiles: list[float],
                            h: int,
                            train_end_date: str):
    """
    Executes an Out-of-Sample Expanding Window backtest.
    Avoids look-ahead bias by dynamically adjusting the training window.

    Returns:
    - realized_oos: 1D array with the actual realized returns.
    - params_oos: 2D array with daily [a, b, loc, scale] JSU parameters.
    """

    if controls is None: controls = []
        
    df_work = df.copy()
    if not isinstance(df_work.index, pd.DatetimeIndex):
        df_work.index = pd.to_datetime(df_work.index)
        
    # 1. Prepare variables and shifted target
    cols_to_keep = [y, x] + controls
    df_work = df_work[cols_to_keep]
    target_col = f"{y}_target_h{h}"
    df_work[target_col] = df_work[y].shift(-h)
    
    # 2. Define the Out-of-Sample testing dates
    # Drop ultimate NaNs (the future that hasn't happened yet)
    df_eval = df_work.dropna(subset=[target_col]).copy()
    
    # Test period starts AFTER the train_end_date
    test_dates = df_eval.loc[df_eval.index > train_end_date].index
    
    if len(test_dates) == 0:
        raise ValueError("The train_end_date leaves no valid Out-of-Sample dates to evaluate.")
        
    params_list = []
    realized_list = []
    
    # 3. Expanding Window Loop
    for current_date in tqdm(test_dates, desc=f"OOS Backtesting (h={h})"):
        
        # --- LOOK-AHEAD BIAS PREVENTION ---
        # At 'current_date', we are trying to predict Y_{t+h}.
        # The latest fully realized historical pair we can use for training 
        # is the one from 'h' periods ago. 
        train_cutoff_idx = df_work.index.get_loc(current_date) - h
        if train_cutoff_idx < 0:
            continue # Not enough history to train
            
        cutoff_date = df_work.index[train_cutoff_idx]
        df_train = df_eval.loc[:cutoff_date].dropna().copy()
        
        # Features available TODAY and the future REALIZED target
        features_t = df_work.loc[[current_date]]
        realized_val = features_t[target_col].values[0]
        
        # --- QUANTILE FORECASTING ---
        forecast_q = []
        for q in quantiles:
            with warnings.catch_warnings():
                warnings.simplefilter("ignore")
                try:
                    reg = q_reg(df=df_train, x=x, y=target_col, tau=q, controls=controls, vcov="robust", max_iter=1000)
                except ValueError:
                    reg = q_reg(df=df_train, x=x, y=target_col, tau=q, controls=controls, vcov="iid", max_iter=1000)
                    
                pred = reg.predict(exog=features_t).values[0]
                forecast_q.append(pred)
                
        # --- PARAMETRIC DISTRIBUTION FITTING (MDE) ---
        try:
            params = mde_jsu(fcq=forecast_q, tau_levels=quantiles)
        except Exception:
            # Fallback to NaNs if optimization fails for a specific stressful day
            params = [np.nan, np.nan, np.nan, np.nan]
            
        params_list.append(params)
        realized_list.append(realized_val)
        
    realized_arr = np.array(realized_list)
    params_arr   = np.array(params_list)
    return realized_arr, params_arr


